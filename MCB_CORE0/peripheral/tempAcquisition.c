struct ModBusFrame{
    unsigned char DeviceAddr;
    unsigned char FuntionCode;
    unsigned char StartAddrMSB;
    unsigned char StartAddrLSB;
    unsigned char ReadNumMSB;
    unsigned char ReadNumLSB;
    unsigned char CRCMSB;
    unsigned char CRCLSB;
};

static void ModBusFrameCreate(struct ModBusFrame *frame,unsigned char DeviceAddr,unsigned char FunctionCode,
                              unsigned short StartAddr,  unsigned char ReadNum)
{
    frame->DeviceAddr   = DeviceAddr;
    frame->FuntionCode  = FunctionCode;
    frame->StartAddrMSB = (StartAddr >> 8);
    frame->StartAddrLSB = (StartAddr & 0XFF) - 1;
    frame->ReadNumMSB   = 0;
    frame->ReadNumLSB   = ReadNum;
    unsigned char crcCode[2] = {0};
    CRC16((unsigned char *)frame, 6, crcCode);
    frame->CRCMSB       = crcCode[1];
    frame->CRCLSB       = crcCode[0];
    return;
}
