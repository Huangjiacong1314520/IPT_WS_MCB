int TDReadBottomHalf(unsigned char Sfpchannel,unsigned char Rs485pin,unsigned char ReadNum,double *buf)
{
    int r;
    unsigned int RecvBuflen = 3+2*ReadNum + 2;
    unsigned char RecvBuf[RecvBuflen];
    memset(RecvBuf, 0, RecvBuflen);
    r = OgRs485RecvData(Sfpchannel, Rs485pin, RecvBuf);
   // r = OgRs485RecvData(Sfpchannel, Rs485pin, RecvBuf);
    int i = 0;
    unsigned char *pbuf = RecvBuf+3;
    if (r == 0)
        goto error;
    if (RecvBuf[2] != 2*ReadNum)
        goto error;
    for (i = 0;i < ReadNum; ++i) {
        unsigned char MSB = pbuf[0];
        unsigned char LSB = pbuf[1];
        union Be2Le temp;
        temp.byte[0] = LSB;
        temp.byte[1] = MSB;
        buf[i] = temp.value/65535.0*(RH-RL) + RL;
        pbuf += 2;
    }
error:
    SetErrorCode((SystemError)(Rs485pin+6));
    return -1;
}
int TDWrite(unsigned char Sfpchannel,unsigned char Rs485pin,unsigned char StartAddr,unsigned char WriteNum,unsigned char value)
{
    unsigned char buflen = 1+1+2+2+1+2*WriteNum+2;
    unsigned char buf[buflen];
    buf[0] = 0x1;
    buf[1] = 0x10;
    buf[2] = 0x0;
    buf[3] = StartAddr-1;
    buf[4] = 0x0;
    buf[5] = WriteNum;
    buf[6] = 2*WriteNum;
    int i;
    for (i = 0;i < WriteNum;++i) {
        buf[7+2*i+1] = value;
    }
    unsigned char CrcCode[2] ={0};
    CRC16(buf, 7+2*WriteNum, CrcCode);
    buf[buflen-1] = CrcCode[0];
    buf[buflen-1] = CrcCode[1];
    OgRs485SendData(Sfpchannel, Rs485pin, buf, buflen);
    return 0;
}
